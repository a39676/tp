package job_test.east.documentGenerate.reapal.domain;

/**
 * 东方星空,param类,属性字段对象
 */
public class ParamAttr {
	
	private String commentPrefix;
	private String annotation;
	private String authorityPrefix;
	private String attrName;
	private String attrChineseName;
	private String dataType;
	private boolean isNecessary;
	
	public String getCommentPrefix() {
		return commentPrefix;
	}
	public void setCommentPrefix(String commentPrefix) {
		this.commentPrefix = commentPrefix;
	}
	public String getAnnotation() {
		return annotation;
	}
	public void setAnnotation(String annotation) {
		this.annotation = annotation;
	}
	public String getAuthorityPrefix() {
		return authorityPrefix;
	}
	public void setAuthorityPrefix(String authorityPrefix) {
		this.authorityPrefix = authorityPrefix;
	}
	public String getAttrName() {
		return attrName;
	}
	public void setAttrName(String attrName) {
		this.attrName = attrName;
	}
	public String getAttrChineseName() {
		return attrChineseName;
	}
	public void setAttrChineseName(String attrChineseName) {
		this.attrChineseName = attrChineseName;
	}
	public String getDataType() {
		return dataType;
	}
	public void setDataType(String dataType) {
		this.dataType = dataType;
	}
	public boolean getIsNecessary() {
		return isNecessary;
	}
	public void setIsNecessary(boolean isNecessary) {
		this.isNecessary = isNecessary;
	}
	
}
